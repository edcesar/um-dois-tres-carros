Em linhas gerais, a tecnologia que envolve carros semi-autônomos abrange vários sistemas distintos que já estão presentes nos carros que compramos atualmente. O principal objetivo de um carro semi-autônomo é propiciar ao motorista uma experiência melhor em relação a segurança e conforto.

 

